public class calculator {
    static int add(int a, int b) {
        return a + b;
    }
    static int sub(int a, int b) {
        return a - b;
    }
    static int multi(int a, int b) {
        return a * b;
    }
  

    public static void main(String[] args) {
        int a = add(2, 3);
        System.out.println(a);
    }
}